    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        DefaultTableModel d1=(DefaultTableModel)Table1.getModel();
        int selectIndex=Table1.getSelectedRow();
        int id=Integer.parseInt(d1.getValueAt(selectIndex,0).toString());

        int dialogResult=JOptionPane.showConfirmDialog(null, "Do You want to delete the row","warning",JOptionPane.YES_NO_OPTION);

        if(dialogResult==JOptionPane.YES_OPTION)
        {
            try{

                Class.forName("com.mysql.cj.jdbc.Driver");

                Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/sheduler","root","");

                String sql = "Delete from shedule where sl_no= '"+id+"' ";

                System.out.println(sql);

                Statement stmt=con.createStatement();
                int rs = stmt.executeUpdate(sql);
                System.out.println(rs);
                if(rs==1)
                {
                    JOptionPane.showMessageDialog(null, "Deleted successfully");
                    fetch();
                    clear();
                }
            }
            catch(Exception e)
            {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

