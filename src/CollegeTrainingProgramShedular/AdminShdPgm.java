    public void fetch()
    {
        try
        {
         Class.forName("com.mysql.cj.jdbc.Driver");  
         Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/sheduler","root","");
         String sql="select * from shedule";
         System.out.println(sql);
         PreparedStatement pst = con.prepareStatement(sql);
         ResultSet rs = pst.executeQuery();
         DefaultTableModel dl=(DefaultTableModel)Table1.getModel();
         dl.setRowCount(0);
         while(rs.next())
            {
                Vector v2=new Vector();

                    v2.add(rs.getString("sl_no"));
                    v2.add(rs.getString("clg"));
                    v2.add(rs.getString("place"));
                    v2.add(rs.getString("prgm"));
                    v2.add(rs.getString("day"));
                    v2.add(rs.getString("fees"));
                    v2.add(rs.getString("pymt_status"));
                    dl.addRow(v2);
            }   
        }
      catch(Exception a)
        {
            System.out.println(a);
        }
    }
